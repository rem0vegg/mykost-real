/**
 * Try to match a Nominatim-returned kota string to our standardized list.
 * Returns the matched list entry, or empty string if not found.
 */
export function matchKotaFromNominatim(geocodedKota, list = KOTA_LIST) {
  if (!geocodedKota) return '';

  const strip = (s) =>
    s.toLowerCase().replace(/^(kota|kabupaten|kab\.?)\s+/i, '').trim();

  const target = strip(geocodedKota);

  // 1. Exact match (case-insensitive)
  const exact = list.find((k) => k.toLowerCase() === geocodedKota.toLowerCase());
  if (exact) return exact;

  // 2. Match after stripping Kota/Kabupaten prefix on both sides
  const stripped = list.find((k) => strip(k) === target);
  if (stripped) return stripped;

  // 3. Partial: list entry name starts with target (e.g. target = "jakarta selatan")
  const partial = list.find((k) => strip(k).startsWith(target) || target.startsWith(strip(k)));
  if (partial) return partial;

  return '';
}
